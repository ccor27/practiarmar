    private void rellenarCampos() {
        txtId.setText(String.valueOf(p.getId()));
        jformatPrecioManoObra.setText(decimalFormat.format(p.getPrecioManoDeObra()));
        jformatPrecioPorEmpleado.setText(decimalFormat.format(p.getPrecioPorEmpleado()));
        jformatPrecioMateriales.setText(decimalFormat.format(p.getPrecioMateriales()));
        txtCliente.setText(cliente.getNombre());
        txtEstado.setText(p.getEstadoProyecto().toString());
        txtAreaDescripcion.setText(p.getDescripcion());
        txtFechaEntrega.setText(String.valueOf(p.getFechaEntrega()));
        txtUltimaModificacion.setText(String.valueOf(p.getFehcaModificacion()));
        jformarPrecioTotal.setText(decimalFormat.format(p.getPrecioTotal()));

    }

    private void rellenarTablaEmpleados() {
        if (empleados != null && !empleados.isEmpty()) {
            Object rowData[] = new Object[1];
            for (Empleado empleado : empleados) {
                rowData[0] = empleado.getNombre();
                //TODO: agregar el codigo del empleado
                modelEmpleados.addRow(rowData);
            }
        }
    }

    private void rellenarTablaFotos() {
        if (fotos != null && !fotos.isEmpty()) {
            Object rowData[] = new Object[1];
            for (Foto foto : fotos) {
                rowData[0] = foto.getNombre();
                modelFotos.addRow(rowData);
            }
        }
    }

    private void rellenarTablaItems() {
        if (items != null && !items.isEmpty()) {
            Object rowData[] = new Object[2];
            for (Item item : items) {
                rowData[0] = item.getNombre();
                rowData[1] = item.getCantidad();
                modelItems.addRow(rowData);
            }
        }
    }

