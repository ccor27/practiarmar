    private void tblFotosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblFotosMouseClicked
        //TODO: mostrar la foto en una ventana nueva, y que hasta no cerrar la venta, no dejar hacer ninguna accion
        String nombreFoto = (String) modelFotos.getDataVector().elementAt(tblFotos.convertRowIndexToModel(tblFotos.getSelectedRow())).get(0);
        for (Foto foto : fotos) {
            if (foto.getNombre().equals(nombreFoto)) {
                
                VerFotoView fotoView = new VerFotoView(menu, true);
                fotoView.setLocationRelativeTo(null);
                fotoView.setFoto(foto);
                fotoView.showFoto();
                fotoView.setVisible(true);
                break;
            }
        }


    }//GEN-LAST:event_tblFotosMouseClicked

    private void tblItemsMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblItemsMouseClicked
        String nombreItem = (String) modelItems.getDataVector().elementAt(tblItems.convertRowIndexToModel(tblItems.getSelectedRow())).get(0);
        for (Item item : items) {
            if(item.getNombre().equals(nombreItem)){
               VerItemView itemView = new VerItemView(menu, true);
               itemView.setLocationRelativeTo(null);
               itemView.setITem(item);
               itemView.mostrarDatos();
               itemView.setVisible(true);
               break;
            }
        }
    }//GEN-LAST:event_tblItemsMouseClicked


