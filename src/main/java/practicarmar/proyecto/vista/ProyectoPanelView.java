    private void llenarTablaProyectos() {
        modelo.setRowCount(0);
        obtenerProyectos();
       if(proyectos!=null && !proyectos.isEmpty()){
            Object rowData[] = new Object[5];
        for (Proyecto p : proyectos) {
            rowData[0] = p.getId();
            rowData[1] = p.getFechaEntrega();
            rowData[2] = saberEstado(p.getEstadoProyecto());
            modelo.addRow(rowData);
        }
       }
    }

    private String saberEstado(EstadoProyecto estado) {
        return switch (estado) {
            case CANCELADO -> "CANCELADO";
            case ESPERA_DE_PAGO -> "ESPERA DE PAGO";
            case FINALIZADO -> "FINALIZADO";
            case EN_CURSO -> "EN CURSO";
            case SIN_EMPEZAR -> "SIN EMPEZAR";
            default -> "";
        };

    }

    private void obtenerProyectos() {
        this.proyectos = controller.obtenerProyectos();
    }

