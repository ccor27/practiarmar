    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        if(proyecto!=null){
            try {
                proyecto.setDeleted(true);
                controller.removeProyecto(proyecto);
                JOptionPane.showMessageDialog(this, "Proyecto eliminado con exito!");
                proyecto=null;
                llenarTablaProyectos();
            } catch (NonexistentEntityException ex) {
                Logger.getLogger(ProyectoPanelView.class.getName()).log(Level.SEVERE, null, ex);
                
            JOptionPane.showMessageDialog(this, "Ocurrio un error al intentar eliminar el proyecto!\n"+ex.getLocalizedMessage());
            }
        }else{
            JOptionPane.showMessageDialog(this, "Debe seleccionar un proyecto!");
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

