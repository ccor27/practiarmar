    public void llenarTablaClientes() {
        model.setRowCount(0);
        obtenerClientes();
        Object rowData[] = new Object[5];
        for (Cliente cliente : clientes) {
            rowData[0] = cliente.getId();
            rowData[1] = cliente.getNombre();
            rowData[2] = cliente.getTelefono();
            rowData[3] = cliente.getEmail();
            rowData[4] = cliente.isRecibirNotificaciones() ? "Si" : "No";
            model.addRow(rowData);
        }
    }

    private void obtenerClientes() {
        this.clientes = controller.obtenerClientes();
    }

