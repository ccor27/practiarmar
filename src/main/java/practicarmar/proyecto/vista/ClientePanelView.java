    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        if (clienteSeleccionado != null) {
            menu.showClienteEditar(clienteSeleccionado);
        } else {
            JOptionPane.showMessageDialog(null, "Debe seleccionar un cliente!");
        }

    }//GEN-LAST:event_btnEditarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        if (clienteSeleccionado != null) {
            try {
                clienteSeleccionado.setDeleted(true);
                controller.removeCliente(clienteSeleccionado);
            } catch (Exception ex) {
                Logger.getLogger(ClientePanelView.class.getName()).log(Level.SEVERE, null, ex);
                JOptionPane.showMessageDialog(null, "Ocurio un error al intentar eliminar el cliente!\n"+ex.getLocalizedMessage());

            }
            llenarTablaClientes();
        } else {
            JOptionPane.showMessageDialog(null, "Debe seleccionar un cliente!");
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void tblClientesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblClientesMouseClicked
        Long id = (Long) model.getDataVector().elementAt(tblClientes.convertRowIndexToModel(tblClientes.getSelectedRow())).get(0);
        for (Cliente cliente : clientes) {
            if (Objects.equals(cliente.getId(), id)) {
                clienteSeleccionado = cliente;
            }
        }
    }//GEN-LAST:event_tblClientesMouseClicked

    private void txtFiltroKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtFiltroKeyReleased
        String filtro = txtFiltro.getText();
        TableRowSorter<DefaultTableModel> tr = new TableRowSorter<>(model);
        tblClientes.setRowSorter(tr);
        tr.setRowFilter(RowFilter.regexFilter(filtro));
    }//GEN-LAST:event_txtFiltroKeyReleased

    private void txtFiltroKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtFiltroKeyTyped
        char c = evt.getKeyChar();
        if (!Character.isLetterOrDigit(c)) {
            evt.consume();
        }
    }//GEN-LAST:event_txtFiltroKeyTyped


