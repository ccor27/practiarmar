    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        Long id = (Long) model.getDataVector().elementAt(jTable1.convertRowIndexToModel(jTable1.getSelectedRow())).get(0);
        for (Empleado empleado : empleados) {
            if (Objects.equals(empleado.getId(), id)) {
                empleadoSeleccionado = empleado;
            }
        }
    }//GEN-LAST:event_jTable1MouseClicked

    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        if (isEmpleadoSeleccionado()) {
            menu.showEditarEmpleadoView(empleadoSeleccionado);
        } else {
            JOptionPane.showMessageDialog(null, "Debe seleccionar un empleado!");
        }
    }//GEN-LAST:event_btnEditarActionPerformed

