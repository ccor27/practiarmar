    private void llenarTablaEmpleados() {
        model.setRowCount(0);
        obtenerEmpleados();
        Object rowData[] = new Object[5];
        for (Empleado empleado : empleados) {
            rowData[0] = empleado.getId();
            rowData[1] = empleado.getNombre();
            rowData[2] = empleado.getTelefono();
            rowData[3] = empleado.getEmail();
            rowData[4] = empleado.getCodigo();
            model.addRow(rowData);
        }
    }

    private void obtenerEmpleados() {
        this.empleados = controller.obtenerListaEmpleados();
    }

