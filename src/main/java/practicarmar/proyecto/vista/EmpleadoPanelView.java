    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        if (isEmpleadoSeleccionado()) {
            empleadoSeleccionado.setDeleted(true);
            try {
                controller.eliminar(empleadoSeleccionado);
            } catch (Exception ex) {
                Logger.getLogger(EmpleadoPanelView.class.getName()).log(Level.SEVERE, null, ex);
                JOptionPane.showMessageDialog(null, "Ocurrio un error al intentar eliminar el emplado!\n"+ex.getLocalizedMessage());
            }
            llenarTablaEmpleados();
        } else {
            JOptionPane.showMessageDialog(null, "Debe seleccionar un empleado!");
        }
    }//GEN-LAST:event_btnEliminarActionPerformed
/**
 * Metodo para filtrar en la tabla
 * @param evt 
 */
    private void txtFiltroKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtFiltroKeyReleased
        String filtro = txtFiltro.getText();
        TableRowSorter<DefaultTableModel> tr = new TableRowSorter<>(model);
        jTable1.setRowSorter(tr);
        tr.setRowFilter(RowFilter.regexFilter(filtro));
    }//GEN-LAST:event_txtFiltroKeyReleased
/**
 * metodo que solo permite el ingreso de números y letras en le campo de filtro
 * @param evt 
 */
    private void txtFiltroKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtFiltroKeyTyped
         char c = evt.getKeyChar();
    if (!Character.isLetterOrDigit(c)) {
        evt.consume();
    }
    }//GEN-LAST:event_txtFiltroKeyTyped
