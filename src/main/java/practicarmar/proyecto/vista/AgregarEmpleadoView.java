    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        if (validacionCampos()) {
            if (isEmailValid()) {
                System.out.println("guardado con exito desde la ventana");
                String nombre = txtNombre.getText();
                String codigo = txtCodigo.getText();
                String password = String.valueOf(txtPassword.getPassword());
                String telefono = txtTelefono.getText();
                String email = txtEmail.getText();
                String username = txtUsername.getText();

                switch (rol) {
                    case "Empleado":
                    {
                        try {
                            controller.crear(nombre, codigo, password, telefono, email, username, Rol.EMPLEADO);
                            JOptionPane.showMessageDialog(this,"Empleado agregado con exito!");
                            btnLimpiarActionPerformed(evt);
                        } catch (Exception ex) {
                            Logger.getLogger(AgregarEmpleadoView.class.getName()).log(Level.SEVERE, null, ex);
                            System.out.println("entro al catch de error de codigo empleado");
                             JOptionPane.showMessageDialog(this,"No es posible que dos empleados tengan el mismo codigo, por favor cambielo\n");
                        }
                    }
                               break;

                           
                    case "Administrador":
                    {
                        try {
                            controller.crear(nombre, codigo, password, telefono, email, username, Rol.ADMINISTRADOR);
                            JOptionPane.showMessageDialog(this,"Empleado agregado con exito!");
                            btnLimpiarActionPerformed(evt);
                        } catch (Exception ex) {
                            Logger.getLogger(AgregarEmpleadoView.class.getName()).log(Level.SEVERE, null, ex);
                            System.out.println("entro al catch de error de codigo empleado");
                            JOptionPane.showMessageDialog(this,"No es posible que dos empleados tengan el mismo codigo, por favor cambielo\n");
                        }
                    }
                        break;


                    default:
                        JOptionPane.showMessageDialog(this, "Ocurrio un error al guardar los datos!");
                }
                
            } else {
                JOptionPane.showMessageDialog(this, "Debe ingresar un email valido!");
            }
        } else {
            JOptionPane.showMessageDialog(this, "Debe rellenar todos los campos!");
        }
    }//GEN-LAST:event_btnGuardarActionPerformed
    private boolean isEmailValid() {
        String EMAIL_PATTERN = "^[_A-Za-z0-9-\\+]+(\\.[_A-Za-z0-9-]+)*@"
                + "[A-Za-z0-9-]+(\\.[A-Za-z0-9]+)*(\\.[A-Za-z]{2,})$";
        return txtEmail.getText().matches(EMAIL_PATTERN);
    }

    private boolean validacionCampos() {
        if (txtNombre.getText().equals("") || txtCodigo.getText().equals("")
                || String.valueOf(txtPassword.getPassword()).isEmpty() || txtTelefono.getText().equals("")
                || txtUsername.getText().equals("") || !isRolSeleccionado() || txtEmail.getText().equals("")) {
            return false;
        } else {
            return true;
        }
    }

    private boolean isRolSeleccionado() {
        rol = (String) comboRol.getSelectedItem();
        if (rol.equalsIgnoreCase("Seleccione un rol")) {
            rol = "";
            return false;
        } else {
            return true;
        }
    }
