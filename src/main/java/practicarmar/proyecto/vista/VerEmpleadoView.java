    private void mostrarDatos() {
        txtId.setText(String.valueOf(e.getId()));
        txtNombre.setText(e.getNombre());
        txtTelefono.setText(e.getTelefono());
        txtCodigo.setText(e.getCodigo());
        txtUsername.setText(e.getUsername());
        txtPassword.setText(e.getContrasena());
        txtEmail.setText(e.getEmail());
        txtRol.setText(e.getRol().toString());
    }

    private void rellenarTablaPago() {
        if (pagos != null && !pagos.isEmpty()) {
            Object rowData[] = new Object[4];
            for (PagoEmpleado pago : pagos) {
                rowData[0] = pago.getId();
                rowData[1] = pago.getCodigoProyecto();
                rowData[2] = pago.getTotal();
                rowData[3] = pago.getEstado();
                model.addRow(rowData);
            }
        }
    }

