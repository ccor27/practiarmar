    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        if (validacionCampos()) {
            if (isEmailValid()) {
                String nombre = txtNombre.getText();
                String codigo = txtCodigo.getText();
                String password = String.valueOf(txtPassword.getPassword());
                String telefono = txtTelefono.getText();
                String email = txtEmail.getText();
                String username = txtUsername.getText();
                switch (rol) {
                    case "Empleado":
                        empleadoAEditar.setNombre(nombre);
                        empleadoAEditar.setCodigo(codigo);
                        empleadoAEditar.setContrasena(password);
                        empleadoAEditar.setTelefono(telefono);
                        empleadoAEditar.setEmail(email);
                        empleadoAEditar.setUsername(username);
                        empleadoAEditar.setRol(Rol.EMPLEADO);
                         {
                            try {
                                controller.editar(empleadoAEditar);
                                JOptionPane.showMessageDialog(this, "Modificacion con exito!");
                                btnLimpiarActionPerformed(evt);
                                btnCancelarActionPerformed(evt);
                            } catch (Exception ex) {
                                Logger.getLogger(EditarEmpleadoView.class.getName()).log(Level.SEVERE, null, ex);
                                JOptionPane.showMessageDialog(this, "Ocurrio un error al editar el empleado!\n" + ex.getLocalizedMessage());
                            }
                        }
                        break;

                    case "Administrador":
                        empleadoAEditar.setNombre(nombre);
                        empleadoAEditar.setCodigo(codigo);
                        empleadoAEditar.setContrasena(password);
                        empleadoAEditar.setTelefono(telefono);
                        empleadoAEditar.setEmail(email);
                        empleadoAEditar.setUsername(username);
                        empleadoAEditar.setRol(Rol.ADMINISTRADOR);
                         {
                            try {
                                controller.editar(empleadoAEditar);
                                JOptionPane.showMessageDialog(this, "Modificacion con exito!");
                                btnLimpiarActionPerformed(evt);
                                btnCancelarActionPerformed(evt);
                            } catch (Exception ex) {
                                Logger.getLogger(EditarEmpleadoView.class.getName()).log(Level.SEVERE, null, ex);
                                JOptionPane.showMessageDialog(this, "Ocurrio un error al editar el empleado!\n" + ex.getLocalizedMessage());
                            }
                        }
                        break;

                    default:
                        JOptionPane.showMessageDialog(this, "Ocurrio un error al guardar los datos!");
                }

            } else {
                JOptionPane.showMessageDialog(this, "Debe ingresar un email valido!");
            }
        } else {
            JOptionPane.showMessageDialog(this, "Debe rellenar todos los campos!");
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

